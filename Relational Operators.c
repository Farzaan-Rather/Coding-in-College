//Relational Operators
//Take two variables and perform all the relational operators.
//among those two Variables



#include <stdio.h>
int main () {
    int a,b;
    scanf("%d %d",&a,&b);
    printf("%d\n",a>b);
    printf("%d\n",a<b);
    printf("%d\n",a=b);
    printf("%d\n",a>=b);
    printf("%d\n",a<=b);
    printf("%d\n",a!=b);





    return 0;








}
